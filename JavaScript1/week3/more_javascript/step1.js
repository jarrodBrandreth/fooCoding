/*
  1. Create a function that takes 3 arguments and returns the sum of these arguments.
*/

function sumOfThree(num1,num2,num3) {
  return num1 + num2 + num3;
}

console.log(sumOfThree(3,4,5)); //expected output 12
console.log(sumOfThree(100,4,22)); //expected output 126
console.log(sumOfThree(25,-25,7)); //expected output 7 